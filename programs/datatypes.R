# Logical
v <- TRUE 
print(class(v))

# Numeric
v <- 12 
print(class(v))

# Integer
v <- 12L 
print(class(v))

# Complex
v <- 2+5i 
print(class(v))

# Character
v <- "hello" 
print(class(v))

# Character
v <- 'hello' 
print(class(v))

# Raw
v <- charToRaw("Hello")
print(class(v))